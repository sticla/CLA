						<p align="justify">
					Enquanto as obras sociais eram realizadas, surgiam, a partir de
					1987, no meio da mata nativa, as edificações e a infra-estrutura
					para abrigar pessoal e equipamentos do novo centro de lançamento,
					transformando anos de pesquisa em incontestável realidade.<br>
					Devido aos imperiosos requisitos de segurança, um centro de
					lançamento de foguetes requer que, ao redor das áreas onde se
					desenvolvem atividades de risco, possa-se garantir a inexistência
					de ameaças às pessoas não envolvidas nas operações. Por isso,
					desponta como de suma importância a disponibilidade de áreas ao
					longo da costa, constituindo-se numa faixa desabitada, livre de
					circulação de pessoas e suficientemente isolada de possíveis
					expansões urbanas.
				</p>
