				<p align="justify">
					Fora do sítio histórico de Alcântara, definiu-se como de interesse
					operacional a costa leste, numa faixa de aproximadamente 200km2. A
					primeira fase de implantação do CLA prevê instalações logísticas e
					operacionais na parte ao sul dessa faixa. Mais para o norte,
					existem áreas de expansão suficientes para a instalação de outras
					plataformas de lançamento e respectivas infra-estruturas.<br>
					Todavia, para que essa área fosse disponibilizada, haveria
					necessidade de remanejamento dos ocupantes dessa faixa operacional.
					Como forma de minimizar o impacto social, optou-se por efetuar esse
					remanejamento dentro dos limites do próprio município. Com isso,
					foram desapropriados mais 420km2, de maneira que os pequenos
					povoados pudessem ser deslocados e reinseridos na área rural de
					Alcântara.
				</p>

				<p align="justify">Portanto, hoje, tem-se que 620km2 do município
