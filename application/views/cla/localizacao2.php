			<p align="justify">
			
				Alcântara se encontra na Mesorregião do Norte Maranhense e particularmente na Microrregião do Litoral Ocidental Maranhense, na qual ocupa o 3° lugar em extensão. Encravada na grande Área de Proteção Ambiental das Reentrâncias Maranhenses e nos limites da Amazônia Legal, Alcântara é rica em biodiversidade e recursos naturais.
				</p>
				<br>
				<p align="justify">
				
				Tombada pelo Serviço do Patrimônio Histórico e Artístico Nacional em 1948, como monumento nacional, a cidade passou a ser conhecida por suas ruínas, igrejas e um vasto acervo histórico-cultural.
Essa região, conhecida até o início dos anos 80 apenas pela sua história e pelo seu acervo arquitetônico, veio a despontar como um cenário privilegiado para a implantação do novo centro de lançamento de foguetes do Brasil.</p>

<p  align="justify">Criado pela Lei n. ° 24, de 05/08/1836, o município em referência possui, segundo o IBGE (1997), uma área de 1.496,5 km2, o que corresponde a 0,45% da superfície estadual (333.365,6 km2). A este município pertencem as Ilhas do Livramento, do Cajual e das Pacas. A sede municipal se localiza a 4 metros de altitude.</p>
      <p  align="justify"><strong>• Limites</strong><br>
        <br>
 O município de Alcântara limita-se ao Norte com o Oceano Atlântico, ao Oeste com os municípios de Bacurituba, Guimarães, Bequimâo e Peri-Mirim, ao Sul com o município de Cajapió, e ao Leste com os municípios de Cajapió e São Luís, separado deste último, pela baía de São Marcos a uma distância de 22 Km, por via marítima, e de 425 km, por via terrestre.</p>
      <p align="justify"><strong>• Aspectos Demográficos</strong><br>
        <br>
      A população de Alcântara, segundo informações do IBGE, atingiu no ano de 2000 um total de 21.291 pessoas, apresentando em relação a 1996, uma taxa de crescimento anual de 2,92%. Com essa evolução, a densidade demográfica cresce nesse período de 12,65 para 14,2 hab/km.</p>
      <p align="justify"> Os dados do Censo Demográfico /2000 revelam ainda, que a população do município de Alcântara concentra-se na área rural (15.626), representando 73,4% da sua totalidade, índice este, muito acima ao da média estadual que é de 40,5%. Por outro lado, a taxa de urbanização é de 26,6%, superior à observada em 1991 (20,4%). No Estado, em 2000, esta taxa atingiu 59,5%.</p>
	
