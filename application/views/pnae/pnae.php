					<p align="justify">O Programa Nacional de Atividades Espaciais -
						PNAE está inserido no ato de criação da Agência Espacial
						Brasileira - AEB, ocorrido em fevereiro de 1994, sendo sua
						elaboração e sua atualização atribuídas a este órgão. A Política
						Nacional de Desenvolvimento das Atividades Espaciais - PNDAE,
						elaborada em dezembro de 1994, estabelece que o PNAE tenha ampla
						abrangência, e seja constituído por programas de cunho científico,
						de aplicações e de capacitação tecnológica, além de atividades
						voltadas à implantação, manutenção e ampliação de infra-estrutura
						operacional e de apoio às atividades de pesquisa e
						desenvolvimento. Consta do PNAE uma série de programas, onde se
						destacam:</p>
					<ol>
						<li><strong>Aplicações Espaciais</strong> - visa criar meios para
							que a sociedade brasileira possa usufruir, da melhor forma, dos
							recursos possibilitados pelos satélites de aplicações espaciais,
							principalmente nas áreas de Sensoriamento Remoto, Meteorologia,
							Oceanografia, Telecomunicações, Geodésia e Navegação.e</li>
						<li><strong>Satélites e Cargas Úteis </strong>- têm por finalidade
							dotar o país de capacidade própria na concepção, projeto,
							desenvolvimento, fabricação e utilização de satélites, bem como
							de subsistemas específicos para os mesmos</li>
						<li><strong>Veículos Lançadores e Foguetes de Sondagem</strong> -
							objetiva capacitar o País no projeto, desenvolvimento e
							construção de veículos lançadores de satélites, bem como
							lançadores de cargas úteis suborbitais. Objetiva ainda, a fixação
							na indústria da produção de tais veículos e seus sistemas,
							contribuindo para a maior qualificação do parque industrial
							brasileiro e sua integração competitiva no mercado mundial.</li>
						<li><strong>Infra-estrutura Espacial</strong> - visa à
							implantação, a complementação, a ampliação, a atualização e a
							manutenção dos centros e laboratórios que compõem a
							infra-estrutura de apoio às atividades espaciais. Estas unidades
							têm caráter não apenas operacional, mas também de apoio às
							atividades de pesquisa e desenvolvimento contempladas no Programa
							Nacional de Atividades Espaciais - PNAE.</li>
						<li><strong>Ciências Espaciais</strong> - objetiva coordenar,
							apoiar e fomentar projetos e atividades voltados à pesquisa
							básica e aplicada sobre fenômenos da atmosfera e do espaço
							exterior, e em outras áreas do conhecimento, como física dos
							materiais e matemática computacional, que contribuam diretamente
							para o avanço da ciência e da tecnologia espaciais.</li>
						<li><strong>Desenvolvimento em Tecnologias Espaciais</strong> -
							tem por finalidade apoiar a execução de projetos e atividades de
							capacitação em tecnologias espaciais necessárias aos sistemas
							espaciais de interesse nacional. Promove, continuamente, o
							domínio de tecnologias consideradas estratégicas, propicia
							condições para a consolidação e atualização da capacidade de
							engenharia e desenvolvimento tecnológico, atuando em instituições
							de ensino e pesquisa.</li>
						<li><strong>Formação e Aperfeiçoamento de Recursos Humanos</strong>
							- visa coordenar iniciativas em andamento e promover uma série de
							novas outras que atendam às atividades espaciais brasileiras no
							tocante à disponibilidade de recursos humanos capacitados,
							necessários ao cumprimento dos objetivos e metas do PNAE.</li>
						<li><strong>Fomento à Capacitação de Empresas da Indústria
								Nacional</strong> - objetiva promover a crescente capacitação de
							empresas nacionais para participar competitivamente, em todos os
							níveis, do mercado e do fornecimento de produtos e serviços
							espaciais.</li>
					</ol>
					<p align="justify">Como se pode verificar, o Programa Espacial
						Brasileiro não se restringe somente ao VLS, ao CLA e aos
						satélites. Estes são apenas três subprogramas dos programas
						descritos anteriormente. O Primeiro programa espacial brasileiro
						com características efetivas de grande porte e longo prazo foi a
						Missão Espacial Completa Brasileira - MECB, que estabeleceu como
						metas o desenvolvimento de pequenos satélites de aplicação (coleta
						de dados ambientais e sensoriamento remoto), de um veículo
						lançador compatível com aqueles satélites - VLS, bem como da
						implantação de infra-estrutura básica requerida por esses projetos
						- CLA.</p>
					<p align="justify">Define-se assim, a participação do Comando da
						Aeronáutica num empreendimento que colocará o Brasil em posição de
						destaque no cenário internacional.</p>
